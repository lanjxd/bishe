package DAO;

public class Item {
	
	private String itemid;
	private String itemname;
	private String itemcate;
	private String itemcond;
	private String itemprice;
	private String itemcount;
	private String iteminfo;
	private String itemseller;

	public String getitemid() {
		return itemid;
	}
	public void setitemid(String itemid) {
		this.itemid = itemid;
	}
	public String getitemprice() {
		return itemprice;
	}
	public void setitemprice(String itemprice) {
		this.itemprice = itemprice;
	}
	public String getitemname() {
		return itemname;
	}
	public void setitemname(String itemname) {
		this.itemname = itemname;
	}
	public String getitemcate() {
		return itemcate;
	}
	public void setitemcate(String itemcate) {
		this.itemcate = itemcate;
	}
	public String getitemcond() {
		return itemcond;
	}
	public void setitemcond(String itemcond) {
		this.itemcond = itemcond;
	}
	public String getitemcount() {
		return itemcount;
	}
	public void setitemcount(String itemcount) {
		this.itemcount = itemcount;
	}
	public String getiteminfo() {
		return iteminfo;
	}
	public void setiteminfo(String iteminfo) {
		this.iteminfo = iteminfo;
	}
	public String getitemseller() {
		return itemseller;
	}
	public void setitemseller(String itemseller) {
		this.itemseller = itemseller;
	}
}

